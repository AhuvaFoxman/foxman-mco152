package foxman.contacts;

public class Contact {

	private String name;
	private String email;
	private Address address;
	private String phone;

	public String getName() {
		return this.name;
	}

	public String getEmail() {
		return email;
	}

	public Address getAddress() {
		return address;
	}

	public String getPhone() {
		return phone;
	}

}
